import numpy as np

numbers = np.array([1, 2, 3, 4, 5])

print("Массив:", numbers)
print("Сумма элементов:", np.sum(numbers))
print("Среднее значение:", np.mean(numbers))
print("Квадраты элементов:", numbers ** 2)

